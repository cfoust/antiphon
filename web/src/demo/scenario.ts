// ---- cadence (ms) -----------------------------------------------------------
const TICK_MS = 400;
const TOOL_MIN = 4000, TOOL_MAX = 11000; // tool-note ticks while working
const TOOL_FIRST_MAX = 8000;
const MURMUR_MIN = 30000, MURMUR_MAX = 55000; // spoken working murmurs
const FIRST_DONE_MIN = 14000, FIRST_DONE_MAX = 20000; // first completion lands early
const DONE_GAP_MIN = 20000, DONE_GAP_MAX = 40000; // staggered completions
const MAX_WAITING = 2; // never more than this many unheard dings at once
const RETRY_MS = 6000; // completion scheduler backoff when the room is saturated

const between = (a: number, b: number) => a + Math.random() * (b - a);

interface Seat {
  idx: number;
  id: string;
  workBufs: (AudioBuffer | null)[];
  doneBufs: (AudioBuffer | null)[];
  workIdx: number; // rotates through the murmur lines
  doneIdx: number; // rotates through the summary lines
  toolNext: number;
  murmurNext: number;
  lastDoneAt: number; // for least-recently-completed picking
  prevState: string;
}

/**
 * Load the scenario voices and start driving the engine. Returns a stop()
 * for symmetry (the demo page never calls it).
 */
export async function startScenario(engine: Chamber): Promise<() => void> {
  const count = Math.min(DEMO_AGENT_COUNT, engine.agents.length);
  const t0 = performance.now();

  // decode every clip up front; a missing/failed clip degrades gracefully
  // (that murmur is skipped / the summary falls back to the quiet acknowledge)
  const dec = (url: string): Promise<AudioBuffer | null> =>
    engine.loadClip(url).catch(() => null);
  const seats: Seat[] = await Promise.all(
    engine.agents.slice(0, count).map(async (a, idx) => {
      const lines = LINES[a.id] ?? { work: [], done: [] };
      const [workBufs, doneBufs] = await Promise.all([
        Promise.all(lines.work.map((_, n) => dec(`audio/demo/${a.id}_work_${n + 1}.mp3`))),
        Promise.all(lines.done.map((_, n) => dec(`audio/demo/${a.id}_done_${n + 1}.mp3`))),
      ]);
      return {
        idx,
        id: a.id,
        workBufs,
        doneBufs,
        workIdx: Math.floor(Math.random() * Math.max(1, lines.work.length)),
        doneIdx: 0,
        // stagger the room: everyone shows a first sign of life within seconds,
        // murmurs fan out so two voices don't speak over each other at the top
        toolNext: t0 + between(1500, TOOL_FIRST_MAX),
        murmurNext: t0 + 7000 + idx * 9000 + between(0, 6000),
        lastDoneAt: 0,
        prevState: "working",
      };
    }),
  );

  engine.startScripted(); // fade the room in; no random auto-finishing

  let nextDone = t0 + between(FIRST_DONE_MIN, FIRST_DONE_MAX);

  const tick = () => {
    const now = performance.now();
    let waiting = 0;
    for (const s of seats) {
      const N = engine.nodes[s.id];
      if (!N) continue;
      const state = N.state;
      if (state === "done" || state === "summarizing") waiting++;

      // recycled back to working (the engine did it) → re-stagger its life
      if (state === "working" && s.prevState !== "working") {
        s.toolNext = now + between(2000, TOOL_FIRST_MAX);
        s.murmurNext = now + between(9000, 25000);
      }
      s.prevState = state;
      if (state !== "working" || N.snoozed) continue;

      // tool-note ticks: the audible texture of an agent at work (also feeds
      // lastActivity, which keeps the working drone humming)
      if (now >= s.toolNext) {
        s.toolNext = now + between(TOOL_MIN, TOOL_MAX);
        engine.bridgeTool(s.idx);
      }
      // occasional spoken murmur — live-narration style one-shot through the
      // whisper mix (clear when faced, breathy in the ambient bed)
      if (now >= s.murmurNext) {
        s.murmurNext = now + between(MURMUR_MIN, MURMUR_MAX);
        const lines = LINES[s.id]?.work ?? [];
        if (lines.length) {
          const n = s.workIdx % lines.length;
          s.workIdx++;
          const buf = s.workBufs[n];
          engine.bridgeLine(s.idx, "progress", lines[n]);
          if (buf) engine.enqueueProgress(s.idx, buf);
        }
      }
    }

    // completions: staggered, one at a time, never flooding the room
    if (now >= nextDone) {
      if (waiting >= MAX_WAITING) {
        nextDone = now + RETRY_MS; // the user isn't listening — don't pile on
      } else {
        // least-recently-completed working agent finishes next
        const cand = seats
          .filter((s) => engine.nodes[s.id]?.state === "working" && !engine.nodes[s.id]?.snoozed)
          .sort((a, b) => a.lastDoneAt - b.lastDoneAt)[0];
        if (cand) {
          const lines = LINES[cand.id]?.done ?? [];
          const n = lines.length ? cand.doneIdx % lines.length : 0;
          cand.doneIdx++;
          cand.lastDoneAt = now;
          const buf = cand.doneBufs[n] ?? null;
          if (buf) engine.setSummaryClip(cand.idx, buf);
          if (lines[n]) engine.bridgeLine(cand.idx, "done", lines[n]);
          engine.markDone(cand.idx);
          nextDone = now + between(DONE_GAP_MIN, DONE_GAP_MAX);
        } else {
          nextDone = now + RETRY_MS; // everyone's mid-report; try again shortly
        }
      }
    }
  };

  const timer = window.setInterval(tick, TICK_MS);
  return () => window.clearInterval(timer);
}
