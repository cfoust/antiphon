import { DEMO_AGENT_COUNT } from "../agents";
import type { Chamber } from "../audio/engine";

// The scripted demo scenario: exactly the native experience, no talk-back.
// A small roster of agents works audibly — the working drone hums, tool-note
// ticks land every few seconds, and an occasional spoken murmur narrates what
// each is doing — and over time they complete tasks: done ping → you face one
// and linger → accept chime → a SPOKEN summary. Heard agents recycle back to
// working (the engine's RECYCLE_MS) and later complete again with a different
// line, so the room stays perpetually, believably alive.
//
// Voices live in public/audio/demo/<id>_{work,done}_<n>.mp3, generated by
// tools/gen-demo-voices.py — keep LINES below in sync with that script so the
// agent list shows exactly what was said.

interface AgentLines {
  work: string[];
  done: string[];
}

/** Mirror of tools/gen-demo-voices.py — the text of each generated clip. */
